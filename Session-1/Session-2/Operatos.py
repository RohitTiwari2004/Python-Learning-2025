#Arithmetic Operators examples
print(5*6)
print(5/2)
print(5//2) #Inter-division Operator
print(5**2) #t0 the power 
print(5%2) #remainder remains value will be the output

#Relational Operator

print(4<5)
print(4>5)
print(4<=5)
print(4>=5)
print(4==5)
print(4!=5)

#Logical Operator
print(1 and 0)

print(1 or 0)

print(not 1)

#bitwise operator

print(2 & 3) #bitwise AND

print(5 | 7)  #bitwise OR

print(5 ^ 7)  #bitwise XOR

print(~4)

print(4 >> 2) #bitwise left operator

print(4 << 2) #bitwise right operator 

